package kr.co.itsone.route52.entity;

public class RestoreEntity {
    private String day;
    private String cosNm;
    private String time;
    private String locker;
    private String name;
    private String slNo;
    private String totalAmt;
    private String chkInNo;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getCosNm() {
        return cosNm;
    }

    public void setCosNm(String cosNm) {
        this.cosNm = cosNm;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLocker() {
        return locker;
    }

    public void setLocker(String locker) {
        this.locker = locker;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSlNo() {
        return slNo;
    }

    public void setSlNo(String slNo) {
        this.slNo = slNo;
    }

    public String getTotalAmt() {
        return totalAmt;
    }

    public void setTotalAmt(String totalAmt) {
        this.totalAmt = totalAmt;
    }

    public String getChkInNo() {
        return chkInNo;
    }

    public void setChkInNo(String chkInNo) {
        this.chkInNo = chkInNo;
    }
}
